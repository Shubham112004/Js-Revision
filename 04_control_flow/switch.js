// switch

const month = 2
switch (month) {
    case 1:
        console.log("Jan");
        break;
    case 2:
        console.log("FEb");
        break;
    case 3:
        console.log("Mar");
        break;
    case 4:
        console.log("Apr");
        break;

    default:
        console.log("This month is not defined");
        break;
}